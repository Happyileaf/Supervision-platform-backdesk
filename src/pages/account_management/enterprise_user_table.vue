      <el-button v-waves class="filter-item" type="primary" icon="el-icon-search" @click="handleFilter">
        {{ $t('table.search') }}
      </el-button>
      <el-button class="filter-item" style="margin-left: 10px;" type="primary" icon="el-icon-edit" @click="handleCreate">
        {{ $t('table.add') }}
      </el-button>
      <el-button v-waves :loading="downloadLoading" class="filter-item" type="primary" icon="el-icon-download" @click="handleDownload">
        {{ $t('table.export') }}
      </el-button>
    </div>

    <el-table
      :key="tableKey"
      v-loading="listLoading"
      :data="list"
      border
      fit
      highlight-current-row
      style="width: 100%;"
      @sort-change="sortChange"
    >
      <el-table-column :label="$t('序号')" prop="id" sortable="custom" align="center" width="80" :class-name="getSortClass('id')">
        <template slot-scope="{row}">
          <span>{{ row.id }}</span>
        </template>
      </el-table-column>
      <el-table-column :label="$t('账号')" width="180px" align="center">
        <template slot-scope="{row}">
          <!-- <span>{{ row.administrator_account | parseTime('{y}-{m}-{d} {h}:{i}') }}</span> -->
          <span>{{ row.cPersonUsername }}</span>
        </template>
      </el-table-column>
      <el-table-column :label="$t('密码')" width="180px" align="center">
        <template slot-scope="{row}">
          <span class="link-type" @click="handleUpdate(row)">{{ row.cPersonPassword }}</span>
          <!-- <el-tag>{{ row.type | typeFilter }}</el-tag> -->
        </template>
      </el-table-column>
      <el-table-column :label="$t('企业名称')" min-width="150px" align="center">
        <template slot-scope="{row}">
          <!-- <span>{{ row.administrator_account | parseTime('{y}-{m}-{d} {h}:{i}') }}</span> -->
          <span>{{ row.cName }}</span>
        </template>
      </el-table-column>
      <el-table-column :label="$t('法定代表人')" width="100px" align="center">
        <template slot-scope="{row}">
          <span>{{ row.cPersonName }}</span>
        </template>
      </el-table-column>
      <el-table-column v-if="showReviewer" :label="$t('table.reviewer')" width="110px" align="center">
        <template slot-scope="{row}">
          <span style="color:red;">{{ row.reviewer }}</span>
        </template>
      </el-table-column>
      <el-table-column :label="$t('状态')" class-name="status-col" width="100" align="center">
        <template slot-scope="{row}">
          <el-tag :type="row.status | statusFilter">
            {{ row.status }}
          </el-tag>
        </template>
      </el-table-column>
      <el-table-column :label="$t('操作')" align="center" width="230" class-name="small-padding fixed-width">
        <template slot-scope="{row,$index}">
          <el-button type="primary" size="mini" @click="handleUpdate(row)">
            {{ $t('编辑') }}
          </el-button>
          <el-button v-if="row.status!='published'" size="mini" type="success" @click="handleModifyStatus(row,'published')">
            {{ $t('激活') }}
          </el-button>
          <el-button v-if="row.status!='draft'" size="mini" @click="handleModifyStatus(row,'draft')">
            {{ $t('禁用') }}
          </el-button>
          <el-button v-if="row.status!='deleted'" size="mini" type="danger" @click="handleDelete(row,$index)">
            {{ $t('删除') }}
          </el-button>
        </template>
      </el-table-column>
    </el-table>

    <pagination v-show="total>0" :total="total" :page.sync="listQuery.page" :limit.sync="listQuery.limit" @pagination="fetch_project_information_list" />

    <el-dialog :title="textMap[dialogStatus]" :visible.sync="dialogFormVisible">
      <el-form ref="dataForm" :rules="rules" :model="temp" label-position="left" label-width="100px" style="width: 400px; margin-left:50px;">
        <el-form-item :label="$t('账号')" prop="administrator_account">
          <el-input v-model="temp.administrator_account" :disabled="dialogUpdate"/>
        </el-form-item>
        <el-form-item :label="$t('密码')" prop="administrator_password">
          <el-input v-model="temp.administrator_password" />
        </el-form-item>
        <el-form-item :label="$t('企业名称')" prop="enterprise_name">
          <el-input v-model="temp.enterprise_name" :disabled="dialogUpdate"/>
        </el-form-item>
        <el-form-item :label="$t('法定代表人')" prop="legal_representative_name">
          <el-input v-model="temp.legal_representative_name" :disabled="dialogUpdate"/>
        </el-form-item>
        <el-form-item :label="$t('状态')">
          <el-select v-model="temp.status" class="filter-item" placeholder="Please select">
            <el-option v-for="item in statusOptions" :key="item" :label="item" :value="item" />
          </el-select>
        </el-form-item>
        <!-- <el-form-item :label="$t('table.importance')">
          <el-rate v-model="temp.importance" :colors="['#99A9BF', '#F7BA2A', '#FF9900']" :max="3" style="margin-top:8px;" />
